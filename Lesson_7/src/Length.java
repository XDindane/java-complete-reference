// Tento program demonstruje pouziti vlastnosti length pole.
class Length {
        public static void main(String args[]) {
                int a1[] = new int[10];
                int a2[] = {3, 5, 7, 1, 8, 99, 44, -10};
                int a3[] = {4, 3, 2, 1};
        
                System.out.println("Pocet prvku pole a1 je " + a1.length);
                System.out.println("Pocet prvku pole a2 je " + a2.length);
                System.out.println("Pocet prvku pole a3 je " + a3.length);
        }
}
