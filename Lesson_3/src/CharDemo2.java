// Promenne typu char se chovaji jako cela cisla.
class CharDemo2 {
        public static void main(String args[]) {
                char ch1;
                
                ch1 = 'X';
                System.out.println("Promenna ch1 obsahuje " + ch1);
                
                ch1++; // inkrementalni navyseni ch1
                System.out.println("Promenna ch1 je nyni rovna " + ch1);
        }
}
